static
MVOID
_testConvertOtp(const char* filename)
{
    FILE* pFile = fopen(filename, "rb");
    if (pFile)
    {
        CAM_CAL_LSC_MTK_TYPE rOtp;
        fseek(pFile, 0, SEEK_END);
        MUINT32 size = ftell(pFile);
        fseek(pFile, 0, SEEK_SET);
        fread(((MUINT8*)&rOtp) + 4, size, 1, pFile);
        fclose(pFile);

        MUINT32 u4XNum, u4YNum, u4BlkW, u4BlkH, u4LastW, u4LastH;
        u4XNum   = ((rOtp.CapIspReg[1] >> 28) & 0x0000000F);
        u4YNum   = ((rOtp.CapIspReg[1] >> 12) & 0x0000000F);
        u4BlkW   = ((rOtp.CapIspReg[1] >> 16) & 0x00000FFF);
        u4BlkH   = ( rOtp.CapIspReg[1]        & 0x00000FFF);
        u4LastW  = ((rOtp.CapIspReg[3] >> 16) & 0x00000FFF);
        u4LastH  = ( rOtp.CapIspReg[3]        & 0x00000FFF);

        MUINT32 u4Width = (u4BlkW*u4XNum + u4LastW) * 2;
        MUINT32 u4Height = (u4BlkH*u4YNum + u4LastH) * 2;
        ILscTbl rTbl(ILscTbl::GAIN_FIXED, u4Width, u4Height, u4XNum+2, u4YNum+2);
        rTbl.setData(rOtp.CapTable, rTbl.getSize());

        char strFile[512];
        sprintf(strFile, "%s.%dx%d_%dx%d.log", filename,
            rTbl.getConfig().i4ImgWd, rTbl.getConfig().i4ImgHt, rTbl.getConfig().i4GridX, rTbl.getConfig().i4GridY);
        rTbl.dump(strFile);

        sprintf(strFile, "%s.goldenFmt.txt", filename);
        FILE* pGolden = fopen(strFile, "w");
        if (pGolden)
        {
            fprintf(pGolden,
            "SensorGoldenCalTable:{   // SensorGoldenCalTable\n"
            "       PixId:    8,\n"
            "       SlimLscType:    0,\n"
            "       Width:    0,\n"
            "       Height:    0,\n"
            "       OffsetX:    0,\n"
            "       OffsetY:    0,\n"
            "       TblSize:    0,\n");
            fprintf(pGolden,
            "       IspLSCReg:    {0, 0x%08x, 0, 0x%08x, 0},\n", rOtp.CapIspReg[1], rOtp.CapIspReg[3]);
            fprintf(pGolden,
            "       GainTable:    {\n");
            MINT32 i = 0;
            MINT32 i4Size = rTbl.getSize();
            MUINT8* pData = static_cast<MUINT8*>(rTbl.editData());
            for (i = 0; i < i4Size; i++)
            {
                if ((i & 0xF) == 0)
                    fprintf(pGolden, "\n            ");
                fprintf(pGolden, "0x%02x, ", *pData++);
            }
            fprintf(pGolden,
            "\n       },\n"
            "},");
            fclose(pGolden);
        }
        /*
        SensorGoldenCalTable:{   // SensorGoldenCalTable
                   PixId:    8,
                   SlimLscType:    0,
                   Width:    0,
                   Height:    0,
                   OffsetX:    0,
                   OffsetY:    0,
                   TblSize:    0,
                   IspLSCReg:    {0, 0xa0607061, 0, 0x005c0065, 0},
                GainTable:    {
                */
    }
    else
    {
        MY_ERR("Fail to open %s", filename);
    }
}

