enum {
  MTK_VIDEO_TRANSCODER_ERROR_NONE = 0,
  MTK_VIDEO_TRANSCODER_ERROR_UNKNOWN = 0x70000000,
  MTK_VIDEO_TRANSCODER_ERROR_UNSUPPORTED_VIDEO = 0x80000000,
  MTK_VIDEO_TRANSCODER_FAILED_TO_TRANSCODE = 0x80000001,
  MTK_VIDEO_TRANSCODER_ERROR_INVALID_CUT_TIME = 0x80000002
};

 typedef void* Mtk_VideoTranscoder_Context;

 typedef struct {
    const char* input_path;
    const char* output_path;
    signed long int target_width;
    signed long int target_height;
    signed long long int begin_ts;  // ms
    signed long long int end_ts;
    unsigned long target_bit_rate;
    unsigned long target_frame_rate;  // set to 0 to align source clip
 } Mtk_VideoTranscoder_Params;
